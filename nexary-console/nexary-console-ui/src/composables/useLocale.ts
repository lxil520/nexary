const locale = ref<ConsoleLocale>(detectInitialLocale());

export function useLocale() {
  const localeLabel = computed(() => (locale.value === 'zh' ? '中文' : 'English'));

  function setLocale(nextLocale: ConsoleLocale): void {
    locale.value = nextLocale;
    window.localStorage?.setItem(STORAGE_KEY, nextLocale);
    document.documentElement.lang = nextLocale === 'zh' ? 'zh-CN' : 'en';
  }

  function toggleLocale(): void {
    setLocale(locale.value === 'zh' ? 'en' : 'zh');
  }

  function t(key: keyof typeof messages.en): string {
    return messages[locale.value][key] ?? messages.en[key] ?? key;
  }

  function enumLabel(value: string | null | undefined): string {
    if (!value) {
      return locale.value === 'zh' ? '无' : 'NONE';
    }
    const labels = enumLabels[locale.value] as Record<string, string>;
    return labels[value] ?? value;
  }

  function formatTimestamp(value: string | null): string {
    if (!value) {
      return t('events.notRecorded');
    }
    return new Intl.DateTimeFormat(locale.value === 'zh' ? 'zh-CN' : 'en-US', {
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
      second: '2-digit',
    }).format(new Date(value));
  }

  return {
    locale,
    localeLabel,
    setLocale,
    toggleLocale,
    t,
    enumLabel,
    formatTimestamp,
  };
}

function detectInitialLocale(): ConsoleLocale {
  const saved = window.localStorage?.getItem(STORAGE_KEY);
  if (saved === 'zh' || saved === 'en') {
    return saved;
  }
  const browserLocale = navigator.language.toLowerCase();
  const intlLocale = Intl.DateTimeFormat().resolvedOptions().locale.toLowerCase();
  if (browserLocale.startsWith('zh') || intlLocale.startsWith('zh')) {
    return 'zh';
  }
  return 'en';
}

document.documentElement.lang = locale.value === 'zh' ? 'zh-CN' : 'en';
